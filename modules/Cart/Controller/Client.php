namespace Fuse\Mod\Cart\Controller;

class Client implements \Fuse\InjectionAwareInterface
{
    protected $di;

    /**
     * @param mixed $di
     */
    public function setDi($di)
    {
        $this->di = $di;
    }

    /**
     * @return mixed
     */
    public function getDi()
    {
        return $this->di;
    }

    public function register(\Fuse_App &$app)
    {
        $app->get('/cart', 'get_cart', array(), get_class($this));
    }

    public function get_cart(\Fuse_App $app)
    {
        return $app->render('mod_cart_index');
    }
}