namespace Fuse\Mod\Servicedomain;

class ServiceTld implements \Fuse\InjectionAwareInterface
{
    protected $di;

    /**
     * @param mixed $di
     */
    public function setDi($di)
    {
        $this->di = $di;
    }

    /**
     * @return mixed
     */
    public function getDi()
    {
        return $this->di;
    }

    public function findOneByTld($tld)
    {
        return $this->di['db']->findOne('Tld', 'tld = ?', array($tld));
    }
}